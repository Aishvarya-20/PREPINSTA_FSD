function metersToFeet(meters) {
    const feet = meters * 3.281;
    console.log(`${meters} meter is equal to ${feet} feet.`);
}
metersToFeet(24); 
function feetToMeters(feet) {
    const meters = feet * 0.3048;
    console.log(`${feet} feet is equal to ${meters} meters.`);
}
feetToMeters(13); 
